## Two ways to make a string that goes across multiple lines

# Newline character
print(" This newline character\n puts a newline\n every time it is used inside quotes \n")
print("It also prints a space just before starting the contents of the new line.")
print("Note the spaces in the first three sentences.\n")

# Printing with three double quotes
print("""This gets the job done..
however you wanna print.
Look no space in the beginning of the previous two lines..
 Now I need space, please excuse me ..                   whooooooooo (bye!)
""")
